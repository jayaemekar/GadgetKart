import 'package:flutter/material.dart';

TextStyle bodyText() {
  return const TextStyle(
    fontSize: 20,
    color: Color(0xff1E1F1E),
  );
}

TextStyle buttonText() {
  return const TextStyle(
    fontSize: 18,
    color: Colors.white,
  );
}

TextStyle headingText() {
  return const TextStyle(
    fontSize: 25,
    color: Color(0xff1E1F1E),
    fontWeight: FontWeight.bold,
  );
}

TextStyle hintText() {
  return const TextStyle(
    fontSize: 20,
    color: Colors.grey,
  );
}

TextStyle subtitleText() {
  return TextStyle(
    fontSize: 16,
    color: Colors.grey[800],
  );
}
